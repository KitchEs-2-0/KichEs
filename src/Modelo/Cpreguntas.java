public class Cpreguntas {
    private String codPreguntaC;
    private String PreguntaC;
    private String codImagen;
    private String respuesta;

    public Cpreguntas() {
    }

    
    public String getCodPreguntaC() {
        return codPreguntaC;
    }

    public void setCodPreguntaC(String codPreguntaC) {
        this.codPreguntaC = codPreguntaC;
    }

    public String getPreguntaC() {
        return PreguntaC;
    }

    public void setPreguntaC(String PreguntaC) {
        this.PreguntaC = PreguntaC;
    }

    public String getCodImagen() {
        return codImagen;
    }

    public void setCodImagen(String codImagen) {
        this.codImagen = codImagen;
    }

    public String getRespuesta() {
        return respuesta;
    }

    public void setRespuesta(String respuesta) {
        this.respuesta = respuesta;
    }
    
    
}
